char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0' )
	{
		while (str[i] != '4')
		{
				if (!i || str[i - 1] == ' ')
				{
					str[i] &= ~(1 << 5);
				}
				else if(str[i - 1] == '-' || str[i - 1] == '+')
				{
					str[i] &= ~(1 << 5);
				}
		}
		i++;
	}
	return (str);
}
